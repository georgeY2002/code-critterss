import javax.persistence.*;
import java.util.HashSet;
import java.util.Objects;
import java.util.Set;

@Entity
public class Course {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)  // Use MySQL's AUTO_INCREMENT for ID generation
    private Long id;  // Unique ID for each course

    private String name;  // Name of the course
    private double rating;  // Rating of the course (e.g., out of 5)
    private int estimatedHours;  // Estimated time to complete the course
    private String description;  // Description of the course

    @ManyToMany(mappedBy = "courses")  // Many-to-Many relationship with User
    private Set<User> users = new HashSet<>();  // Users enrolled in this course

    // Constructors
    public Course() {}

    public Course(String name, double rating, int estimatedHours, String description) {
        this.name = name;
        this.rating = rating;
        this.estimatedHours = estimatedHours;
        this.description = description;
    }

    // Getters and Setters

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getRating() {
        return rating;
    }

    public void setRating(double rating) {
        this.rating = rating;
    }

    public int getEstimatedHours() {
        return estimatedHours;
    }

    public void setEstimatedHours(int estimatedHours) {
        this.estimatedHours = estimatedHours;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Set<User> getUsers() {
        return users;
    }

    public void setUsers(Set<User> users) {
        this.users = users;
    }

    // Equals and HashCode

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Course course = (Course) o;
        return Objects.equals(id, course.id);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id);
    }
}
